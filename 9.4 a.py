# Чтение количества запросов
q = int(input())
stack = []

# Обработка запросов
for _ in range(q):
    query = input().strip().split()
    
    if query[0] == "1":  # Запрос первого типа
        x = int(query[1])
        stack.append(x)  # Добавляем x на вершину стека
        print(stack[-1])  # Выводим верхний элемент стека
    
    elif query[0] == "2":  # Запрос второго типа
        if stack:  # Если стек не пуст
            stack.pop()  # Удаляем верхний элемент
            if stack:  # Если после удаления стек не пуст
                print(stack[-1])  # Выводим новый верхний элемент
            else:
                print(-1)  # Стек пуст
        else:
            print(-1)  # Стек пуст (по условию задачи это не должно произойти)